    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
    String username = tUsn.getText();
    String password = new String(tPass.getPassword());

    try (Connection conn = dbkoneksi.getConnection()) {
        String sql = "SELECT username, namapegawai FROM pegawai WHERE username = ? AND password = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, username); 
        pst.setString(2, password);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            
            String namaPegawai = rs.getString("namapegawai");
            // Login berhasil
            kasir kasir = new kasir();
            kasir.setNamaPegawai(namaPegawai);
            kasir.setVisible(true);
            
            // Simpan ke session

            this.dispose(); // tutup form login
        } else {
            JOptionPane.showMessageDialog(this, "Login gagal! Username atau password salah.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Kesalahan koneksi: " + e.getMessage());
        e.printStackTrace();
    }    }//GEN-LAST:event_btLoginActionPerformed

